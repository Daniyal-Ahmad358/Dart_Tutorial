void main() {
  // TERNARY OPERATOR
  /*
    Syntax:

    (condition)? if_true : if_false;
  */
  // It works like if else and returns value based on condition.

  // Example 1:
  var num1 = 5;
  var num2 = 10;

  var ans = (num1 > num2) ? true : false;

  print(ans);

  // Example 2:
  var x = 1;
  var y = 2;

  var z = (x > y) ? 'up' : 'down';

  print(z);
}
