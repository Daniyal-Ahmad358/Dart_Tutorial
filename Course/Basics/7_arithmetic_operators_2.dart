import 'dart:math';

void main() {
  // We can use external libraries(i.e. math)

  // Find Surface Area of a Cube, Formula: 6 * ( length ^ 2 )

  var length = 5;
  var surface_area = 6 * pow(length, 2);

  print('Surface Area: $surface_area');

  // Assignment Operator "=", i.e.
  var assignment_operator = 5;
  print('Assigned: $assignment_operator');
}
